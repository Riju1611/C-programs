// 3. Multiplying two numbers.
#include <stdio.h>
int main()
{
    int num1,num2,product;
    printf("Enter 1st number:");
    scanf("%d",&num1);
    printf("Enter 2nd number:");
    scanf("%d",&num2);
    product=num1*num2;
    printf("The product of %d and %d is %d", num1, num2, product);
    return 0;

}
